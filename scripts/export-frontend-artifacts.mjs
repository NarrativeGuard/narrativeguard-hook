import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const artifacts = {
  NarrativeGuardHook: "artifacts/contracts/NarrativeGuardHook.sol/NarrativeGuardHook.json",
  NarrativeGuardHookHarness:
    "artifacts/contracts/test/NarrativeGuardHookHarness.sol/NarrativeGuardHookHarness.json",
  MockPoolManager: "artifacts/contracts/test/MockPoolManager.sol/MockPoolManager.json",
  DemoMemeToken: "artifacts/contracts/DemoMemeToken.sol/DemoMemeToken.json",
};

const exported = {};

for (const [name, artifactPath] of Object.entries(artifacts)) {
  const artifact = JSON.parse(await readFile(artifactPath, "utf8"));
  exported[name] = {
    abi: artifact.abi,
    bytecode: artifact.bytecode,
  };
}

const output = `// Generated by scripts/export-frontend-artifacts.mjs. Do not edit by hand.
export const contracts = ${JSON.stringify(exported, null, 2)};
`;

const outputPath = path.join("frontend", "src", "generated", "contracts.js");
await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, output);
console.log(`Wrote ${outputPath}`);
