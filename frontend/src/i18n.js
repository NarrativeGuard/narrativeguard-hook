const DICTIONARY = { en, zh };

export function createTranslator(language) {
  const dict = DICTIONARY[language] ?? en;
  return (key, values = {}) => {
    const template = dict[key] ?? en[key] ?? key;
    return template.replace(/\{(\w+)\}/g, (_, name) => String(values[name] ?? ""));
  };
}
